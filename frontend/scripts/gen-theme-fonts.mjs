#!/usr/bin/env node
// T-16a1 P4 — theme FONT whitelist generator (the single source of truth for
// "which --font-* token a theme bundle may re-value, and which font-family
// stack strings it may set them to").
//
// Unlike the colour whitelist (whose SSOT is theme.css and whose VALUES pass a
// grammar), a font value is NOT admitted by grammar — a font-family stack has
// no safe closed grammar that also excludes url()/@font-face — so the value set
// is itself a CLOSED ALLOWLIST. styles/themeFonts.source.json IS that contract:
// a hand-curated list of `{id,label,stack}` safe families plus the --font-*
// token names. This script EXTRACTS both and emits two committed generated
// files (the same regen-and-diff discipline as gen-theme-tokens / gen-message-keys):
//
//   * frontend/src/styles/themeFonts.generated.ts — THEME_FONT_TOKENS +
//     SAFE_FONT_FAMILIES, the client + mock validation whitelist AND the editor
//     dropdown's option list.
//   * server/ocserverd/theme_fonts_gen.go — themeFontTokens + themeFontStacks,
//     the server validation whitelist.
//
// Both are checked into the tree and pinned by a CI drift gate (bin/ci.sh):
// change themeFonts.source.json without re-running `npm run gen:fonts` and CI
// goes red.
//
// ⚠️ The Go output filename is theme_fonts_gen.go — it MUST NOT contain the
// substring `_token` (the ci.sh hygiene denylist rejects tracked filenames
// carrying it; T-16a1 P2 hit this with theme_colornames_gen.go).
//
// Run: `npm run gen:fonts` (or `node scripts/gen-theme-fonts.mjs`).

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = join(HERE, "..", "..");
const SRC = join(ROOT, "frontend", "src", "styles", "themeFonts.source.json");
const TS_OUT = join(ROOT, "frontend", "src", "styles", "themeFonts.generated.ts");
const GO_OUT = join(ROOT, "server", "ocserverd", "theme_fonts_gen.go");

const src = JSON.parse(readFileSync(SRC, "utf8"));
const tokens = [...new Set(src.tokens)].sort();
const families = src.families ?? [];

if (tokens.length === 0 || families.length === 0) {
  console.error("[gen-theme-fonts] source has no tokens or no families — aborting");
  process.exit(1);
}

// Defensive: the same structure-breaking characters the value validators reject.
// If a curated stack ever smuggles one in, fail the GENERATOR (never ship a
// whitelist entry that the twin validators would then reject at runtime).
const FORBIDDEN = [
  "url(", "expression(", "var(", "javascript:", "/*", "*/",
  ";", "{", "}", "<", ">", "@", "\\", "`", "\n", "\r", "(",
];
for (const f of families) {
  if (!f.id || !f.label || !f.stack) {
    console.error(`[gen-theme-fonts] family missing id/label/stack: ${JSON.stringify(f)}`);
    process.exit(1);
  }
  for (const m of FORBIDDEN) {
    if (f.stack.includes(m)) {
      console.error(`[gen-theme-fonts] family ${f.id} stack contains forbidden ${JSON.stringify(m)} — aborting`);
      process.exit(1);
    }
  }
}

const ids = new Set();
for (const f of families) {
  if (ids.has(f.id)) {
    console.error(`[gen-theme-fonts] duplicate family id ${f.id} — aborting`);
    process.exit(1);
  }
  ids.add(f.id);
}

// Families keep SOURCE ORDER (the editor dropdown order); the value set is
// derived from it. Sorting the Go stack map keeps the emitted file byte-stable.
const stacks = [...new Set(families.map((f) => f.stack))].sort();

const banner = (tool) =>
  `// Code generated by frontend/scripts/gen-theme-fonts.mjs — DO NOT EDIT.\n` +
  `// Source of truth: frontend/src/styles/themeFonts.source.json.\n` +
  `// Regenerate: npm run gen:fonts (${tool}).\n`;

const ts =
  banner("this file") +
  `\n/** The whitelist of --font-* token names a theme bundle's optional \`fonts\`\n` +
  ` * map may re-value. Client + mock validation whitelist. */\n` +
  `export const THEME_FONT_TOKENS: readonly string[] = [\n` +
  tokens.map((t) => `  ${JSON.stringify(t)},`).join("\n") +
  `\n];\n\n` +
  `/** One safe, curated font-family stack. \`stack\` is the ONLY string a bundle\n` +
  ` * may store for a font token; \`label\` is the friendly name shown in the\n` +
  ` * theme editor's font dropdown. The value set (the \`stack\`s) is a CLOSED\n` +
  ` * allowlist — never an arbitrary string. */\n` +
  `export interface SafeFontFamily {\n` +
  `  readonly id: string;\n` +
  `  readonly label: string;\n` +
  `  readonly stack: string;\n` +
  `}\n\n` +
  `export const SAFE_FONT_FAMILIES: readonly SafeFontFamily[] = [\n` +
  families
    .map(
      (f) =>
        `  { id: ${JSON.stringify(f.id)}, label: ${JSON.stringify(
          f.label
        )}, stack: ${JSON.stringify(f.stack)} },`
    )
    .join("\n") +
  `\n];\n`;

// gofmt aligns the values of a map literal (tabwriter, padding 1). Replicate it
// so the emitted file is gofmt-clean and the CI drift gate stays byte-stable:
// pad each `"key":` out to the widest, then a single space + value.
function goMap(name, comment, keys) {
  const keyParts = keys.map((k) => `${JSON.stringify(k)}:`);
  const widest = Math.max(...keyParts.map((k) => k.length));
  return (
    comment +
    `var ${name} = map[string]bool{\n` +
    keyParts.map((k) => `\t${k}${" ".repeat(widest - k.length + 1)}true,`).join("\n") +
    `\n}\n`
  );
}

const go =
  banner("server/ocserverd/theme_fonts_gen.go") +
  `\npackage main\n\n` +
  goMap(
    "themeFontTokens",
    `// themeFontTokens is the whitelist of --font-* token names a theme bundle's\n` +
      `// optional \`fonts\` map may re-value (theme_bundle.go / font_bundle.go\n` +
      `// validation). Extracted from frontend/src/styles/themeFonts.source.json.\n`,
    tokens
  ) +
  `\n` +
  goMap(
    "themeFontStacks",
    `// themeFontStacks is the CLOSED allowlist of font-family stack strings a font\n` +
      `// value may be. A value outside this set is a 422 — never an arbitrary string.\n`,
    stacks
  );

writeFileSync(TS_OUT, ts);
writeFileSync(GO_OUT, go);
console.log(
  `[gen-theme-fonts] wrote ${tokens.length} tokens + ${families.length} families →\n  ${TS_OUT}\n  ${GO_OUT}`
);
